# --- Imports ---

import gradio as gr

from .mkd import in_txt, in_txt2
from .utz import header1

# --- GLobal Vars ---

# Choosing the themes
themes = [
    gr.themes.Ocean(),
    gr.themes.Monochrome(),
    gr.themes.Citrus(),
    gr.themes.Glass(),
    gr.themes.Default(),
    gr.themes.Soft()
]
c_th = themes[5]


# --- Main Function ---


def g1_main():
    g1_1()


# --- Sub Function ---

# General tab testing here
def g1_1():
    header1("Gradio Test 1.1")

    # Introduction section
    def intro_section():
        gr.Markdown(in_txt)

    # Main Interface
    with gr.Blocks(
        theme=c_th
    ) as g1_ui:

        with gr.Tab("Gradio Test 1.1"):
            intro_section()

    g1_ui.launch(
        show_error=True,
    )

