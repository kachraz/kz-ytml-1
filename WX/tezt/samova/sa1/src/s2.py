# Litellm Example from docs
# https://docs.sambanova.ai/cloud/docs/integrations/litellm#litellm

def lite1():
    header1("LiteLLM Example from SN docs")

    os.environ['SAMBANOVA_API_KEY'] = ""

    response = completion(
        model="sambanova/Meta-Llama-3.2-3B-Instruct",
        messages=[
            {
                "role": "user",
                "content": "What do you know about SambaNova Systems, answer like a gangster",
            }
        ],
        max_tokens=10,
        stop=[],
        temperature=0.2,
        top_p=0.9,
        user="user",
    )
    print(response)
    save_to_markdown(
        response,
        prefix="lite1",
        directory="rez"
    )
